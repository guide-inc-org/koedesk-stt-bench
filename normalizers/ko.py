"""韓国語テキスト正規化 — PREREGISTRATION.md §5 (Normalization) の韓国語部分を実装する。

対応する仕様セクション:
    §5 手順1-4  (共通パイプライン: NFKC / ケースフォールド / 記号除去 / 空白畳み込み)
    §5.5        (数値等価性: 漢数語→算用数字、桁区切りカンマ除去)
    §5.6 (ko)   (「ko / vi / id / ru: steps 1–5 plus whitespace-delimited WER」
                 = 追加の言語固有規則なし。WERトークナイザは空白分割)
    AMENDMENTS.md Amendment 1 スコープ注記 (2026-07-05):
                 「ko keeps §5.4 as frozen」— 韓国語正書法は分かち書き (単語間空白)
                 を持つため、ja/zh/th に導入された「空白全除去」の**対象外**。
                 §5.4 凍結どおり空白は「連続を単一スペースへ畳み込み、前後を strip」
                 のみを行い、分かち書きの差異は正書法上の実差として WER/CER に
                 そのまま乗せる。したがって ja.py の strip_all_whitespace_for_cer に
                 相当するフラグはこの実装には意図的に**存在しない**。

--------------------------------------------------------------------------
【凍結仕様のあいまい性 — 語中アポストロフィ例外の適用範囲 (両論併記)】

§5 手順3は「Unicode カテゴリ P*/S* を除去、ただし word-internal apostrophes in
**Latin-script languages** are kept」と定める。韓国語はラテン文字言語ではないが、
エンジン出力にはラテン文字の英語借用語 ("let's" 等) が混在しうる。

  読みA (字義どおり): 例外は「言語」単位でスコープされており、ko は非該当。
      → アポストロフィは例外なくすべて除去する。
  読みB (トークン単位): 例外の趣旨は英語縮約形の保護なので、ko テキスト中の
      ラテン文字語にも適用する。

本実装は **読みA を採用** する。理由: 凍結文言は例外の適用対象を明示的に
「Latin-script languages」という言語の属性で限定しており、これをトークン単位へ
拡張するのは凍結後の規則追加にあたる (§5.6 は追加規則に「見出し実測前の
コード+テストの公開」を要求しており、最小限の字義解釈が最も安全)。
リファレンス・仮説の両側に同一規則が適用されるため公平性は保たれる。
実際上の影響も軽微 (韓国語正書法にアポストロフィは存在しない)。

--------------------------------------------------------------------------
【実装上の解釈が必要だった箇所 — あいまい性ではなく、仕様の例を満たすための唯一解】

桁区切り除去 (§5.5) と記号除去 (§5 手順3) の順序:
    ja.py / en.py と同一の論点。桁区切りのカンマ (Unicode category "Po", P*) を
    先に一般記号除去するとカンマがトークン境界 (空白) 化し「33,000」→「33 000」
    となり、§5.5 が明示する等価性「33,000→33000」を満たせない。よって
    「数字に挟まれたカンマの完全削除」を一般記号除去より前に実行する。
    韓国語の桁区切り慣習はカンマ (33,000)、小数点はピリオド。小数 ("3.5") は
    §5.5 の対象範囲 (整数 0–99,999) 外であり、ピリオドは手順3の一般記号除去で
    トークン境界になる (レンジのハイフン 25-30→25 30 と同じ「宣言済みノイズ」
    クラス、§5.5 のレンジ規則に準ずる)。

--------------------------------------------------------------------------
【§5.5 漢数語 (한자어 수사) → 算用数字 変換の保守的ルール】

対象範囲: 0〜99,999 の整数 (仕様どおり)。変換対象の文字集合:
    영 공 (0)  일 이 삼 사 오 육 칠 팔 구 (1-9)  십 백 천 (位)  만 (万)

韓国語の数詞には2系統ある:

  (1) 漢数語 (일/이/삼…+십/백/천/만): 位取り合成は日本語の漢数字と同型
      (삼만삼천 = 33000)。ただし日本語と決定的に違うのは、漢数語の各音節が
      **ごく普通の非数詞形態素と同綴り** である点 (일=仕事/日、이=この/歯/主格助詞、
      사=会社/4、오=来る/5、구=9/句、만=〜だけ/万、백=白/100、천=布/1000…)。
      無条件変換は一般語彙を大量に破壊するため、下記の構造規則で変換範囲を絞る。

  (2) 固有語数詞 (하나/둘/셋/넷/다섯…열/스물…、連体形 한/두/세…): 助数詞と
      結合して用いられ多義性が高い (하나=「一つ(のもの)」、열=10/熱、
      한=1/おおよそ/「した」…)。日本語の助数詞あいまいさ (一人/ひとり) より
      一段強い**体系的**多義なので、タスク方針どおり **系統ごと丸ごと
      曖昧クラス=不変換** とする (§5.5「曖昧クラスは両側とも不変換で列挙」)。
      固有語数詞は上記の変換対象文字集合に一切含まれないため、構造的に
      変換されない (test_ko.py で列挙・アサート)。

変換条件 (すべて満たす run のみ変換; 決定論的・文脈判定なし):
  1. run (漢数語文字と ASCII 数字の最大連続列) が位文字 (십/백/천/만) を
     少なくとも1つ含む。
     → 桁読み表記 (이공이육=2026 のような1音節=1桁の連結) は **不変換**。
       理由: 数詞音節のみで構成される一般語が多数ある (사이=間、오이=きゅうり、
       이사=引っ越し、구이=焼き物…) ため、桁読みクラスは丸ごと曖昧クラスに置く。
       (ja の 二〇二六 は専用数字文字なので変換できたが、ko では不可能)
  2. run の長さが2以上。
     → 単音節 run (일/이/…/십/백/천/만 単独) は **不変換**。同綴り異義が
       支配的なため (십년=10年 の「십」も巻き添えで不変換になるが、
       保守線の宣言済みノイズとして許容; ref/hyp 対称)。
  3. run 全体が語彙化ブロックリストと完全一致しない:
         만일 (「もしも」= 万一; ja の 万一 と同じ慣用句)
         만이 (「〜だけが」= 補助詞 만 + 主格助詞 이 の連鎖)
         천사 (「天使」; 数値としては 1004)
         만사 (「万事」; 数値としては 10004)
         오만 (「傲慢」/国名オマーン; 数値としては 50000) — 2026-07-05 追加。
             FLEURS ko 見出しセットの実測破損 [31] 오만하다→50000하다 への
             対処。ja/zh は「run+直後1文字」連結のブロックリストも持つが、
             ko は既存機構の流儀 (run 完全一致) に合わせて完全一致で登録する:
             直後文脈方式 (오만+하 のみブロック) は 오만한/오만했다/오만함 等の
             活用形を取りこぼすため採らない。トレードオフとして数値読みの
             오만 (오만 원=5万ウォン) も不変換になるが、ref/hyp 双方同一規則の
             宣言済みノイズとして許容する (십분→십분 と同じ保守線)。なお
             오만삼천 (=53000) のような長い run の部分文字列としての 오만 は
             完全一致でないため従来どおり変換される。
  4. run の開始境界が語頭相当である (2026-07-05 追加 — FLEURS ko 実測破損
     [27] 돌연변이만이→돌연변20002 への対処):
     run が (i) 文字列先頭・空白直後・非ハングル文字の直後から始まる、
     (ii) 序数接頭辞 제 の直後から始まる (제이십→제20 の凍結挙動を維持)、
     (iii) run の先頭文字が ASCII 数字である (약3만・제21과 の 21 のような
     数字先頭 run はハングル語中の偽陽性になり得ない)、のいずれかを満たす。
     → 어절 (空白区切り単位) 内部・ハングル語中の途中から始まる純ハングル
       run (돌연변이만이 の「이만이」等) を構造的に排除する。既知の限界:
       제 で終わる一般語 (문제 等) の直後から始まる run は引き続き変換対象に
       なりうる (宣言済みノイズ; ref/hyp 対称)。
  5. 厳密パースに成功し、値が 0〜99,999 に収まる。パース規則:
     - 만 で前後分割し 前×10000+後 (前が空なら 1: 만삼천=13000)。
       만 が2回以上現れる run (만만하다 の「만만」等) はパース失敗=不変換
       (ja パイロットの「1万万」ハルシネーション対処と同じフォールバック)。
     - 십/백/천 の直前係数は **漢数語1音節のみ** 許す。漢数語2音節以上の係数
       (이삼십・삼사십 のような概数複合 = 「2,30」「30,40」) はパース失敗=
       不変換。末尾の裸の桁も同様 (십이삼=「12,3」は不変換、이십오=25 は変換)。
       ASCII 数字由来の複数桁係数 (3천500=3500 のような混在表記) は概数では
       ないので許す。
     - 値の範囲チェックが 천만 (=10^7 >99,999) を自動的に除外するため、
       「천만에요 (どういたしまして)」の「천만」はブロックリスト不要で不変換。

序数の扱い:
  - 漢数語序数 제N (第N): ja の「第二十→第20」と同型に、接頭辞 제 は文字列に
    残したまま後続の数字部分のみを変換する (제이십→제20)。제 は変換対象文字
    集合に含まれないため、特別な処理なしで run 検出が自然にこの挙動になる。
    副次効果として、語彙化した 제일 (「最も」= 第一)・제이 (第二) は後続 run が
    単音節のため条件2で自然に不変換となる (曖昧クラスとしてテストで列挙)。
  - 固有語序数 첫째/둘째/셋째… は固有語系統ごと不変換 (変換対象文字を含まない)。

既知の残存限界 (ja.py の「統一」「一切」相当; 意図的に許容し宣言する):
  - 이만 (「これで」=20000) のような「2音節+位文字」の語彙化熟語は変換されて
    しまう (어절頭から始まるため変換条件4でも拾えない)。数値読みも現実に
    高頻度なため一律ブロックはせず、実データで問題化したらブロックリスト
    追記+テスト列挙の運用とする (個別に結果を見て事後判断しない、という
    検証原則)。同クラスだった 오만 は FLEURS ko 見出しセットで実際に破損が
    確認されたため、この運用どおり 2026-07-05 にブロックリストへ追記済み
    (変換条件3)。어절内部の途中から始まるクラス (돌연변이만이) は変換条件4で
    構造的に解消済み。
  - 韓国語正書法は数を万単位で分かち書きする (삼만 삼천)。本実装は空白を
    跨いだ数の結合をしない (それぞれ独立に変換: 삼만 삼천→30000 3000)。
    空白跨ぎ結合は「만 이천」等で助詞との新たな曖昧性を生むため、決定論優先で
    見送り。ref/hyp 対称の宣言済みノイズ。(Amendment 1 のスコープ注記により
    ko は全空白除去の対象外なので、ja/zh/th の Amendment 3 (空白除去を数値
    変換より前へ) も ko には存在しない。)
  - 「일만이 넘는다 (1万を超える)」の 이 (主格助詞) のように、位文字を含む
    正当な数値 run の直後に数詞音節と同綴りの助詞が続くと run に取り込まれる
    (일만이→10002。run は어절頭から始まるため変換条件4では拾えない)。
    同上の運用で対処する。
"""

from __future__ import annotations

import re
import unicodedata

# --- 漢数語の文字集合 (§5.5) -----------------------------------------------------

_DIGIT_MAP = {
    "영": 0, "공": 0,
    "일": 1, "이": 2, "삼": 3, "사": 4, "오": 5,
    "육": 6, "칠": 7, "팔": 8, "구": 9,
}
_UNIT_MAP = {"십": 10, "백": 100, "천": 1000}
_MYRIAD = "만"
_UNIT_CHARS = "".join(_UNIT_MAP) + _MYRIAD

_NUM_CHARS = "".join(_DIGIT_MAP) + _UNIT_CHARS
# ASCII数字も混在run (例: 3만3000) を拾うために文字クラスへ含める。
_NUMBER_RUN_RE = re.compile(f"[0-9{_NUM_CHARS}]+")

# §5.5 の対象範囲上限。
_MAX_VALUE = 99_999

# 数詞音節のみで構成される語彙化した語のブロックリスト (run 完全一致でのみ適用。
# 長い run の部分文字列としての出現、例: 십만일천=101000相当、はブロックしない)。
# 오만 (傲慢/オマーン) は FLEURS ko 実測破損 [31] により 2026-07-05 追加
# (方式選定の理由・오만 원 のトレードオフはモジュールdocstring 変換条件3を参照)。
_IDIOM_BLOCKLIST = {"만일", "만이", "천사", "만사", "오만"}

# 序数接頭辞 제 (変換条件4 (ii): 제이십→제20 の凍結挙動を維持するための例外)。
_ORDINAL_PREFIX = "제"

_WHITESPACE_RE = re.compile(r"\s+")
# 桁区切りカンマ (数字に挟まれたもののみ)。NFKC 後なので全角カンマは考慮不要。
_DIGIT_GROUP_SEP_RE = re.compile(r"(?<=\d),(?=\d)")


def _parse_span(part: str) -> int:
    """만 を含まない (=既に分割済みの) 数字文字列を整数へ変換する。

    ASCII数字だけの場合はそのまま int()。
    십/백/천 のいずれも含まなければ「桁読み」(1文字=1桁の連結) とみなす。
    ※ 桁読み run は _should_convert の条件1 (位文字必須) で既に除外されている
      ため、この分岐が通常到達するのは 만 分割後の断片のみ。
    십/백/천 を含む場合は位取り計算を行う (例: 삼천오백 → 3500、이십오 → 25)。
    概数複合 (이삼십 등、漢数語2音節以上の係数/末尾桁) は ValueError で
    パース失敗させる (モジュールdocstring 変換条件5を参照)。
    """
    if part == "":
        return 0
    if part.isdigit():
        return int(part)

    if not any(c in _UNIT_MAP for c in part):
        # 桁読み: 各文字を1桁の数字文字列に変換して連結する。
        # 만 の残骸などパース不能文字が混ざれば int() が ValueError を上げる。
        digit_str = "".join(
            str(_DIGIT_MAP[c]) if c in _DIGIT_MAP else c for c in part
        )
        return int(digit_str)

    # 位取り計算。
    value = 0
    pending = ""            # 位文字の直前に置かれた係数
    pending_has_hangul = False
    for ch in part:
        if ch.isdigit():
            pending += ch
        elif ch in _DIGIT_MAP:
            pending += str(_DIGIT_MAP[ch])
            pending_has_hangul = True
        elif ch in _UNIT_MAP:
            if pending_has_hangul and len(pending) > 1:
                # 이삼십 (=2,30) のような概数複合。変換しない。
                raise ValueError(f"approximate compound: {part!r}")
            value += (int(pending) if pending else 1) * _UNIT_MAP[ch]
            pending = ""
            pending_has_hangul = False
        else:
            raise ValueError(f"unparseable char {ch!r} in {part!r}")
    if pending:
        if pending_has_hangul and len(pending) > 1:
            # 십이삼 (=12,3) のような概数複合の末尾。変換しない。
            raise ValueError(f"approximate trailing digits: {part!r}")
        value += int(pending)
    return value


def _parse_number_run(run: str) -> int:
    """만 を含みうる完全な数字run文字列を整数へ変換する (0〜99,999想定)。"""
    if _MYRIAD in run:
        before, _, after = run.partition(_MYRIAD)
        if _MYRIAD in after:
            # 만 の重複 (만만하다 の「만만」等) はパース不能=不変換。
            raise ValueError(f"duplicated myriad: {run!r}")
        before_val = _parse_span(before) if before else 1
        after_val = _parse_span(after) if after else 0
        return before_val * 10000 + after_val
    return _parse_span(run)


def _is_hangul_syllable(ch: str) -> bool:
    """完成形ハングル音節 (U+AC00–U+D7A3) かどうか。

    変換条件4 (run 開始境界ガード) の「非ハングル文字」判定に使う。FLEURS の
    韓国語テキストは完成形音節で構成されるため、この範囲で十分
    (単独字母 U+1100– 等はテキスト中に現れない前提の保守的判定)。
    """
    return "가" <= ch <= "힣"


def _should_convert(s: str, start: int, end: int) -> bool:
    """run=s[start:end] を変換してよいかを §5.5 の保守的ルール
    (docstring 変換条件1-4) で判定する。"""
    run = s[start:end]
    if run in _IDIOM_BLOCKLIST:
        return False
    if not any(c in _UNIT_CHARS for c in run):
        # 位文字なし = 桁読み表記クラス。丸ごと曖昧クラス (사이/오이/이사…)。
        return False
    if len(run) < 2:
        # 単音節 run (십/백/천/만/일… 単独)。同綴り異義が支配的。
        return False
    # 変換条件4: run 開始境界ガード (2026-07-05 追加)。ハングル語中の途中から
    # 始まる純ハングル run (돌연변이만이 の「이만이」等) を構造的に排除する。
    # 제 の直後 (제이십)・ASCII 数字先頭 (약3만、제21과 の 21) は正当なので許す。
    prev_char = s[start - 1] if start > 0 else ""
    if (
        prev_char
        and _is_hangul_syllable(prev_char)
        and prev_char != _ORDINAL_PREFIX
        and not run[0].isdigit()
    ):
        return False
    return True


def sino_korean_to_arabic(text: str) -> str:
    """漢数語 (および ASCII 数字との混在表記) を算用数字へ変換する。

    0〜99,999 の整数を対象とする決定的コンバータ。変換方針・除外ルールの詳細は
    このモジュールの docstring「§5.5 漢数語→算用数字 変換の保守的ルール」を参照。
    """
    out = []
    last = 0
    for m in _NUMBER_RUN_RE.finditer(text):
        run = m.group(0)
        if not any(c in _NUM_CHARS for c in run):
            # ASCII数字のみのrunは変換対象がないのでスキップ (no-op)。
            continue
        if not _should_convert(text, m.start(), m.end()):
            continue
        try:
            value = _parse_number_run(run)
        except (ValueError, KeyError):
            # パース不能 run は §5.5 の方針 (曖昧・解析不能は触らない) どおり
            # 変換せずそのまま残す。ref/hyp 双方に同一規則が適用される。
            continue
        if not (0 <= value <= _MAX_VALUE):
            # 範囲外 (천만=10^7 等) は不変換。
            continue
        out.append(text[last:m.start()])
        out.append(str(value))
        last = m.end()
    out.append(text[last:])
    return "".join(out)


# --- 記号除去 --------------------------------------------------------------------


def _remove_punctuation_and_symbols(text: str) -> str:
    """Unicode カテゴリ P* (punctuation) / S* (symbol) の文字を除去する。

    語中アポストロフィ例外は適用しない (ko はラテン文字言語ではないため。
    モジュールdocstring「語中アポストロフィ例外の適用範囲」の読みAを参照)。
    ja.py と同様、単語の意図しない融合を避けるためスペースに置換してから
    後段の空白畳み込み (§5 手順4) に委ねる。
    """
    return "".join(
        " " if unicodedata.category(c)[0] in ("P", "S") else c for c in text
    )


# --- メインパイプライン -----------------------------------------------------------


def normalize_ko(text: str) -> str:
    """PREREGISTRATION.md §5 の韓国語正規化パイプライン。

    Amendment 1 スコープ注記により、空白は §5.4 凍結どおり
    「連続を単一スペースへ畳み込み、前後を strip」のみ。
    分かち書き (単語間の単一スペース) はそのまま保存される。
    """
    # 1. Unicode NFKC (全角英数字・互換ハングル字母の統一を含む)
    text = unicodedata.normalize("NFKC", text)

    # 2. ケースフォールド (ハングルには影響しない。埋め込みLatin文字のみ影響)
    text = text.casefold()

    # 3. 桁区切り除去 (§5.5) — 一般記号除去より前に実行する理由は
    #    モジュールdocstring「実装上の解釈が必要だった箇所」を参照。
    text = _DIGIT_GROUP_SEP_RE.sub("", text)

    # 4. 記号・句読点の除去 (§5 手順3、カテゴリ P*/S*。アポストロフィ例外なし)
    text = _remove_punctuation_and_symbols(text)

    # 5. 空白畳み込み (§5 手順4 = §5.4 凍結どおり。全除去はしない: Amendment 1)
    text = _WHITESPACE_RE.sub(" ", text).strip()

    # 6. 漢数語→算用数字変換 (§5.5)
    text = sino_korean_to_arabic(text)

    return text


def tokenize_ko(text: str) -> list[str]:
    """WER計算用トークナイザ。§5.6 (ko) の指定どおり空白分割。

    en.py の tokenize_en と同じ流儀で、内部で normalize_ko() を適用してから
    分割する (生テキストを渡してよい)。
    """
    normalized = normalize_ko(text)
    if not normalized:
        return []
    return normalized.split(" ")
